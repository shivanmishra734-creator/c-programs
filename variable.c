#include<stdio.h>
int main() {
    int a = 10; // This is an integer variable
    float b = 14.4; // This is a floating-point variable
    char c = 's'; // This is a character variable
    printf("the value of a is %d", a); // This line prints the value of the integer variable 'a'
    printf("the value of b is %f", b ); // This line prints the value of the floating-point variable 'b'
    printf("the value of c is %c", c); // This line prints the value of the character variable 'c'
    return 0;
}